import math

x = float(input("Введіть значення х (число, ціле або з дробовою частиною): "))

if x > 0:
    y = 0.5 - x ** (1/4)
elif x == -1:
    pass
else:
    y = ((math.sin(x ** 2)) ** 2) / abs(x + 1)

if x == -1:
    print("Значення х не підходить - не можна ділити на 0")
else:
    print(f"f(x) = {y}")