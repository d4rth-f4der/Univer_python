# Використовуючи інтерпретатор мови Python, знайти значення 16-их FF, 4AB, FAFBCD
hex_number_one = "FF"
hex_number_two = "4AB"
hex_number_three = "FAFBCD"
print("16-ове FF це 10-ове", int(hex_number_one, 16))
print("16-ове 4AB це 10-ове", int(hex_number_two, 16))
print("16-ове FAFBCD це 10-ове", int(hex_number_three, 16))