# Лабораторна робота 1, завдання 3 (ліва частина)
# Записати число 10111 у системах числення з основами P = 1, 8, 16
number = 10111
number_bin = bin(number)
number_oct = oct(number)
number_hex = hex(number)
print("десяткове 10111 це двійкове", number_bin)
print("десяткове 10111 це вісімкове", number_oct)
print("десяткове 10111 це шістнадцяткове", number_hex)