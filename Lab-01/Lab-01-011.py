# Нехай м позначає ціле невід'ємне число. Написати вираз, який задає обчислення
# а) значення молодшої десяткової цифри числа v

v = int(input("Введіть v, ціле невід'ємне число: "))

youngest_decimal = v % 10
print(f"Молодша десяткова цифра числа {v}: {youngest_decimal}")

print(f"""
Бінарне представлення числа {v}: {bin(v)}
Молодший (найправіший) біт: {v % 2}
""")