# Використовуючи інтерпретатор мови Python, знайти число, яке позначає запис 10101, якщо основою системи числення є P = 2, 8, 16
number = 10101
number_bin = bin(number)
number_oct = oct(number)
number_hex = hex(number)
print(f"""
Десяткове число в 10101 в двійковій системі: {number_bin},
в вісімковій: {number_oct},
в шістнадцятковій: {number_hex}
""")