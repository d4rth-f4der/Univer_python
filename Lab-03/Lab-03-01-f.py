a = 1
b = 2
c = b   # c = 2

def f(x, y):
    global c
    a, b = x, y
    c = a + b   # змінить значення зовнішньої с після виклику функції
    return c

b = 3   # зміна значення b
print(a, b, c)  # 1 3 2
print(f(a,a), a, b, c)  # f(1,1)=2 1 3 2(після виклику f(1,1))