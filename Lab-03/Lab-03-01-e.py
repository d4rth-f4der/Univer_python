# Лабораторна 3 завдання 1 (д)

a = 1
b = 2
c = b   # c = 2

def f(x, y):
    global c
    x += 3
    y += 2 + c
    c = a + b + 1   # змінить значення зовнішньої с після виклику функції
    return x + y

b = 1   # зміна значення b
print(a, b, c)  # 1 1 2
print(f(a,b), a, b, c)  # f(1,1)=9 1 1 3(після виклику f(1,1))
