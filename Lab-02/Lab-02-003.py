first_side = int(input("Введіть першу сторону трикутника: "))
second_side = int(input("Введіть другу сторону трикутника: "))
third_side = int(input("Введіть третю сторону трикутника: "))
perimeter = first_side + second_side + third_side
print(f'Периметр трикутника = {perimeter}')